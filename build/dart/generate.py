#!/usr/bin/env python3
"""
openapi.json → packages/dart/lib/src/generated/{tools.dart, api_paths.dart} 생성

Usage:
    python3 build/dart/generate.py
"""
import json
import re
import sys
from pathlib import Path

ROOT = Path(__file__).parent.parent.parent
SPEC_PATH = ROOT / "openapi.json"
GEN_DIR = ROOT / "packages/dart/lib/src/generated"

EXCLUDED_API_IDS = {"au10001", "au10002"}
EXCLUDED_SEGMENTS = {"websocket"}


def segment_from_real_path(real_path: str) -> str:
    parts = [p for p in real_path.strip("/").split("/") if p]
    return parts[-1] if parts else ""


def sanitize(s: str) -> str:
    return re.sub(r"[^a-zA-Z0-9]", "_", s)


def dart_string(s: str) -> str:
    return "'" + s.replace("\\", "\\\\").replace("'", "\\'") + "'"


def dart_map(obj: object, indent: int = 0) -> str:
    pad = "  " * indent
    inner_pad = "  " * (indent + 1)
    if isinstance(obj, dict):
        if not obj:
            return "<String, dynamic>{}"
        items = [f"{inner_pad}{dart_string(k)}: {dart_map(v, indent + 1)}" for k, v in obj.items()]
        return "{\n" + ",\n".join(items) + f",\n{pad}}}"
    if isinstance(obj, list):
        if not obj:
            return "[]"
        items = [f"{inner_pad}{dart_map(v, indent + 1)}" for v in obj]
        return "[\n" + ",\n".join(items) + f",\n{pad}]"
    if isinstance(obj, bool):
        return "true" if obj else "false"
    if isinstance(obj, int):
        return str(obj)
    if isinstance(obj, float):
        return str(obj)
    if obj is None:
        return "null"
    return dart_string(str(obj))


def build_tools(spec: dict) -> list[dict]:
    tools = []
    for methods in spec["paths"].values():
        for op in methods.values():
            api_id = op.get("x-api-id") or op.get("operationId") or ""
            real_path = op.get("x-real-path") or ""
            segment = segment_from_real_path(real_path)

            if api_id in EXCLUDED_API_IDS or segment in EXCLUDED_SEGMENTS:
                continue

            rb_schema = (
                op.get("requestBody", {})
                .get("content", {})
                .get("application/json", {})
                .get("schema", {})
            )
            input_schema: dict = {"type": "object", "properties": rb_schema.get("properties", {})}
            if rb_schema.get("required"):
                input_schema["required"] = rb_schema["required"]

            tag = (op.get("tags") or [segment])[0]
            tool_name = f"kiwoom_{sanitize(segment)}_{sanitize(api_id)}"
            tools.append({
                "toolName": tool_name,
                "apiId": api_id,
                "realPath": real_path,
                "segment": segment,
                "summary": op.get("summary") or api_id,
                "tag": tag,
                "inputSchema": input_schema,
            })

    tools.sort(key=lambda t: t["toolName"])
    return tools


def render_tools(tools: list[dict]) -> str:
    lines = [
        "// AUTO-GENERATED by build/dart/generate.py — do not edit manually",
        "// Re-generate: python3 build/dart/generate.py",
        "",
        "import '../client.dart';",
        "",
        "typedef ToolHandler = Future<Map<String, dynamic>> Function(",
        "    Map<String, dynamic> args);",
        "",
        "class ToolDefinition {",
        "  const ToolDefinition({",
        "    required this.name,",
        "    required this.description,",
        "    required this.inputSchema,",
        "    required this.handler,",
        "  });",
        "  final String name;",
        "  final String description;",
        "  final Map<String, dynamic> inputSchema;",
        "  final ToolHandler handler;",
        "}",
        "",
        "const List<ToolDefinition> kTools = [",
    ]

    for t in tools:
        schema_dart = dart_map(t["inputSchema"], indent=2)
        desc = f"{t['summary']} ({t['apiId']}) — {t['tag']}"
        fn_name = f"_h{sanitize(t['toolName'])}"
        lines += [
            "  ToolDefinition(",
            f"    name: {dart_string(t['toolName'])},",
            f"    description: {dart_string(desc)},",
            f"    inputSchema: {schema_dart},",
            f"    handler: {fn_name},",
            "  ),",
        ]

    lines += ["];", ""]

    for t in tools:
        fn_name = f"_h{sanitize(t['toolName'])}"
        lines += [
            f"Future<Map<String, dynamic>> {fn_name}(Map<String, dynamic> args) =>",
            f"    callKiwoom({dart_string(t['realPath'])}, {dart_string(t['apiId'])}, args);",
            "",
        ]

    return "\n".join(lines)


def render_api_paths(tools: list[dict]) -> str:
    paths = ",\n".join(
        f"  {dart_string(t['apiId'])}: {dart_string(t['realPath'])}" for t in tools
    )
    segs = ",\n".join(
        f"  {dart_string(t['apiId'])}: {dart_string(t['segment'])}" for t in tools
    )
    return "\n".join([
        "// AUTO-GENERATED by build/dart/generate.py — do not edit manually",
        "// Re-generate: python3 build/dart/generate.py",
        "",
        "const Map<String, String> kApiPaths = {",
        paths + ",",
        "};",
        "",
        "const Map<String, String> kApiSegments = {",
        segs + ",",
        "};",
        "",
    ])


def main() -> None:
    spec = json.loads(SPEC_PATH.read_text(encoding="utf-8"))
    tools = build_tools(spec)
    GEN_DIR.mkdir(parents=True, exist_ok=True)

    tools_out = GEN_DIR / "tools.dart"
    tools_out.write_text(render_tools(tools), encoding="utf-8")
    print(f"Generated {len(tools)} tools → {tools_out}", file=sys.stderr)

    paths_out = GEN_DIR / "api_paths.dart"
    paths_out.write_text(render_api_paths(tools), encoding="utf-8")
    print(f"Generated api_paths → {paths_out}", file=sys.stderr)


if __name__ == "__main__":
    main()
