#!/usr/bin/env python3
"""
openapi.json → packages/mcp/src/tools.ts 생성

Usage:
    python build/mcp/generate.py
"""
import json
import re
import sys
from pathlib import Path

ROOT = Path(__file__).parent.parent.parent
SPEC_PATH = ROOT / "openapi.json"
OUT_PATH = ROOT / "packages/mcp/src/tools.ts"

EXCLUDED_API_IDS = {"au10001", "au10002"}
EXCLUDED_SEGMENTS = {"websocket"}


def segment_from_real_path(real_path: str) -> str:
    parts = [p for p in real_path.strip("/").split("/") if p]
    return parts[-1] if parts else ""


def sanitize(s: str) -> str:
    return re.sub(r"[^a-zA-Z0-9]", "_", s)


def json_inline(obj: object, indent: int = 2, base: int = 4) -> str:
    raw = json.dumps(obj, ensure_ascii=False, indent=indent)
    pad = " " * base
    return ("\n" + pad).join(raw.splitlines())


def build_tools(spec: dict) -> list[dict]:
    tools = []
    for methods in spec["paths"].values():
        for op in methods.values():
            api_id = op.get("x-api-id") or op.get("operationId") or ""
            real_path = op.get("x-real-path") or ""
            segment = segment_from_real_path(real_path)

            if api_id in EXCLUDED_API_IDS or segment in EXCLUDED_SEGMENTS:
                continue

            rb_schema = (
                op.get("requestBody", {})
                .get("content", {})
                .get("application/json", {})
                .get("schema", {})
            )
            input_schema: dict = {"type": "object", "properties": rb_schema.get("properties", {})}
            if rb_schema.get("required"):
                input_schema["required"] = rb_schema["required"]

            tag = (op.get("tags") or [segment])[0]
            tool_name = f"kiwoom_{sanitize(segment)}_{sanitize(api_id)}"
            tools.append(
                {
                    "toolName": tool_name,
                    "apiId": api_id,
                    "realPath": real_path,
                    "summary": op.get("summary") or api_id,
                    "tag": tag,
                    "inputSchema": input_schema,
                }
            )

    tools.sort(key=lambda t: t["toolName"])
    return tools


def render(tools: list[dict]) -> str:
    lines = [
        "// AUTO-GENERATED by build/mcp/generate.py — do not edit manually",
        "// Re-generate: python build/mcp/generate.py",
        "",
        'import { CallToolResult } from "@modelcontextprotocol/sdk/types.js";',
        'import { callKiwoom } from "./client.js";',
        "",
        "export interface ToolDefinition {",
        "  name: string;",
        "  description: string;",
        "  inputSchema: Record<string, unknown>;",
        "  handler: (args: Record<string, unknown>) => Promise<CallToolResult>;",
        "}",
        "",
        "export const TOOLS: ToolDefinition[] = [",
    ]

    for t in tools:
        schema_str = json_inline(t["inputSchema"], indent=2, base=6)
        desc = f"{t['summary']} ({t['apiId']}) — {t['tag']}"
        lines += [
            "  {",
            f"    name: {json.dumps(t['toolName'])},",
            f"    description: {json.dumps(desc)},",
            f"    inputSchema: {schema_str},",
            "    async handler(args) {",
            "      const data = await callKiwoom(",
            f"        {json.dumps(t['realPath'])},",
            f"        {json.dumps(t['apiId'])},",
            "        args as Record<string, unknown>",
            "      );",
            "      return {",
            '        content: [{ type: "text", text: JSON.stringify(data, null, 2) }],',
            "      };",
            "    },",
            "  },",
        ]

    lines += ["];", ""]
    return "\n".join(lines)


def main() -> None:
    spec = json.loads(SPEC_PATH.read_text(encoding="utf-8"))
    tools = build_tools(spec)
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUT_PATH.write_text(render(tools), encoding="utf-8")
    print(f"Generated {len(tools)} tools → {OUT_PATH}", file=sys.stderr)


if __name__ == "__main__":
    main()
